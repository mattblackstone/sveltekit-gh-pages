      <h3>Overview</h3>
      <article class="row">
        <div>
          <h4>The Problem</h4>
          <p>
            Mercedes-Benz USA has over 500,000 possible vehicle configurations on their site. The client wanted to
            target users with an exact replica of the vehicle that they configured on the site. The sheer number of
            configurations makes creating an ad for each untenable and would result in a large number of ads that would
            never be served to anyone.
          </p>
        </div>
      </article>
      <article class="row">
        <div>
          <h4>The Solution</h4>
          <p>
            MBUSA Build Your Own site visitors were tagged with a pixel from the dynamic ad serving platform
            Flashtalking to anonymously collect configuration information such as class, model paint, wheels etc. These
            users are later retargeted in display channels with dynamic Mercedes-Benz ad content that features a digital
            replica of the vehicle that they configured. Using a custom developed solution, new vehicle configurations
            are pulled dynamically from the MBUSA media server at impression time, and cached at the dynamic ad server
            for any future requests.
          </p>
        </div>
      </article>
      <article class="row">
        <div>
          <h4>The Result</h4>
          <p>Compared to the generic creative control, the data-driven Build Your Own creative drove:</p>
          <ul>
            <li>60% LIFT in overall view through conversion rate</li>
            <li>73% LIFT in overall click conversion rate</li>
            <li>20% LIFT in overall CTR</li>
          </ul>
